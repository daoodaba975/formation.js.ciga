// Déclaration variables pour les notes

let note1 = 12;
let note2 = 9;
let note3 = 14;

// Calcule de la moyenne
let moyenne = (note1 + note2 + note3) / 3;

// Affichage résultat : "Admis" si la moyenne est supérieure ou égale à 10, sinon "Non admis"
if (moyenne >= 10) {
  console.log("Admis");
} else {
  console.log("Non admis");
}

let jourDeLaSemaine = "lundi";

switch (jourDeLaSemaine) {
  case "lundi":
    console.log("C'est le début de la semaine.");
    break;
  case "mercredi":
    console.log("On est au milieu de la semaine.");
    break;
  case "vendredi":
    console.log("C'est bientôt le week-end.");
    break;
  default:
    console.log("Jour non reconnu.");
}

// Variables et Opérateurs Arithmétiques
// Objectif : Calculer la somme, la différence et le produit de trois nombres.

var a = 10;
var b = -6;
var c = 100;

// la somme des trois nombres
var somme = a + b + c;

// la différence des trois nombres
var diff = a - b - c;

// le produit des trois nombres
var produit = a * b * c;

console.log("La somme est de : " + somme);
console.log("La différence est de : " + diff);
console.log("Le produit est de : " + produit);

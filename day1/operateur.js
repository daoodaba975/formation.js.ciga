// Soustraction de deux nombres
let d = 10;
let e = 5;
let f = d - e;
console.log(f);

// Multiplication de deux nombres
let g = 10;
let h = 5;
let i = g * h;
console.log(i);

// Division de deux nombres
let j = 10;
let k = 5;
let l = j / k;
console.log(l);

// Modulo de deux nombres
let m = 10;
let n = 5;
let o = m % n;
console.log(o);

// Puissance de deux nombres
let p = 10;
let q = 5;
let r = p ** q;
console.log(r);

// Récupération de la racine carre de deux nombres
let s = 10;
let t = 5;
let u = Math.sqrt(s);
let v = Math.sqrt(t);
let w = u * v;
console.log(w);

// Récupération de la racine cubique de deux nombres
let x = 10;
let y = 5;
let z = Math.cbrt(x);
